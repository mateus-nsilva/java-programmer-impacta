package br.com.impacta.inteiros;

public class VariaveisByte {
	public static void main(String[] args) {
		// tipo, nome da variavel
		// = atribuição
		byte idade = 80;
		byte idade2 = 50;
		
		byte soma = (byte) (idade + idade2);
		
		System.out.println("A soma das idades é: " + soma);
	}
}
